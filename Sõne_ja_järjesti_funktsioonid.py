lst = []  # Начальный пустой список

while True:
    print("\nМеню:")
    print("1 - Проверить, состоит ли строка только из пробельных символов (isspace())")
    print("2 - Поменять регистр всех символов (swapcase())")
    print("3 - Проверить, состоит ли строка только из строчных букв (islower())")
    print("4 - Удалить пробелы слева от строки (lstrip())")
    print("5 - Преобразовать строку в заголовочный формат (title())")
    print("6 - Добавить элемент в список (append())")
    print("7 - Удалить последний элемент из списка (pop())")
    print("8 - Найти индекс элемента в списке (index())")
    print("9 - Проверить, состоит ли строка только из заглавных букв (isupper())")
    print("10 - Проверить, состоит ли строка только из цифр (isdigit())")
    print("11 - Показать текущий список")
    print("0 - Выход")

    choice = input("Выберите опцию: ")

    if choice == "0":
        print("Программа завершена.")
        break

    elif choice == "1":
        S = input("Введите строку для проверки (isspace): ")
        # Проверяет, состоит ли строка только из пробельных символов
        print(f"isspace(): {S.isspace()}")

    elif choice == "2":
        S = input("Введите строку для преобразования (swapcase): ")
        # Меняет регистр всех символов строки. Буквы, которые были заглавными, становятся строчными и наоборот
        print(f"swapcase(): {S.swapcase()}")

    elif choice == "3":
        S = input("Введите строку для проверки (islower): ")
        # Проверяет, состоит ли строка только из строчных букв
        print(f"islower(): {S.islower()}")

    elif choice == "4":
        S = input("Введите строку для удаления пробелов слева (lstrip): ")
        # Убирает все пробельные символы (или другие символы, если переданы) с начала строки
        print(f"lstrip(): {S.lstrip()}")

    elif choice == "5":
        S = input("Введите строку для преобразования в заголовочный формат (title): ")
        # Преобразует строку в заголовочный формат. Первый символ каждого слова становится заглавным
        print(f"title(): {S.title()}")

    elif choice == "6":
        elem = input("Введите элемент для добавления в список (append): ")
        # Добавляет новый элемент в конец списка
        lst.append(elem)
        print(f"Элемент {elem} добавлен в список.")

    elif choice == "7":
        if lst:
            popped_item = lst.pop()
            # Удаляет и возвращает последний элемент из списка
            print(f"pop(): {popped_item}")
            print(f"Список после pop(): {lst}")
        else:
            print("Список пуст!")

    elif choice == "8":
        elem = input("Введите элемент для поиска в списке (index): ")
        if elem in lst:
            # Находит индекс первого вхождения указанного элемента в списке
            print(f"index(): {lst.index(elem)}")
        else:
            print("Элемент не найден.")

    elif choice == "9":
        S = input("Введите строку для проверки (isupper): ")
        # Проверяет, состоит ли строка только из заглавных букв
        print(f"isupper(): {S.isupper()}")

    elif choice == "10":
        S = input("Введите строку для проверки (isdigit): ")
        # Проверяет, состоит ли строка только из цифр
        print(f"isdigit(): {S.isdigit()}")

    elif choice == "11":
        # Показывает текущий список
        print(f"Текущий список: {lst}")

    else:
        print("Неверный выбор, попробуйте снова.")
