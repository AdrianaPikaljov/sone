lst = []  # Начальный пустой список

while True:
    print("Меню:")
    print("1 - Проверить, состоит ли строка только из пробельных символов (isspace())")
    print("2 - Поменять регистр всех символов (swapcase())")
    print("3 - Проверить, состоит ли строка только из строчных букв (islower())")
    print("4 - Удалить пробелы слева от строки (lstrip())")
    print("5 - Преобразовать строку в заголовочный формат (title())")
    print("6 - Проверить, состоит ли строка только из заглавных букв (isupper())")
    print("7 - Проверить, состоит ли строка только из цифр (isdigit())")
    print("8 - Показать текущий список")
    print("9 - Разделить строку по символу (split())")
    print("10 - Дополнить строку нулями до заданной ширины (zfill())")
    print("0 - Выход")

    choice = input("Выберите опцию: ")

    if choice == "0":
        print("Программа завершена.")
        break

    elif choice == "1":
        S = input("Введите строку для проверки (isspace): ")
        # Метод isspace() проверяет, состоит ли строка только из пробельных символов.
        print(f"isspace(): {S.isspace()}")

    elif choice == "2":
        S = input("Введите строку для преобразования (swapcase): ")
        # Метод swapcase() меняет регистр всех символов строки. Буквы, которые были заглавными, становятся строчными и наоборот.
        print(f"swapcase(): {S.swapcase()}")

    elif choice == "3":
        S = input("Введите строку для проверки (islower): ")
        # Метод islower() проверяет, состоит ли строка только из строчных букв.
        print(f"islower(): {S.islower()}")

    elif choice == "4":
        S = input("Введите строку для удаления пробелов слева (lstrip): ")
        # Метод lstrip() удаляет все пробельные символы (или другие символы, если указаны) с начала строки.
        print(f"lstrip(): {S.lstrip()}")

    elif choice == "5":
        S = input("Введите строку для преобразования в заголовочный формат (title): ")
        # Метод title() преобразует строку в заголовочный формат. Первый символ каждого слова становится заглавным.
        print(f"title(): {S.title()}")

    elif choice == "6":
        S = input("Введите строку для проверки (isupper): ")
        # Метод isupper() проверяет, состоит ли строка только из заглавных букв.
        print(f"isupper(): {S.isupper()}")

    elif choice == "7":
        S = input("Введите строку для проверки (isdigit): ")
        # Метод isdigit() проверяет, состоит ли строка только из цифр.
        print(f"isdigit(): {S.isdigit()}")

    elif choice == "8":
        # Печатает текущий список (в данный момент пустой).
        print(f"Текущий список: {lst}")

    elif choice == "9":
        S = input("Введите строку для разделения по символу (split): ")
        symbol = input("Введите символ для разделения: ")
        # Метод split() разделяет строку на части, используя указанный символ в качестве разделителя.
        split_result = S.split(symbol)
        print(f"split(): {split_result}")

    elif choice == "10":
        S = input("Введите строку для дополнения нулями (zfill): ")
        width = int(input("Введите желаемую ширину строки: "))
        # Метод zfill() дополняет строку нулями до заданной ширины.
        print(f"zfill(): {S.zfill(width)}")

    else:
        print("Неверный выбор, попробуйте снова.")
